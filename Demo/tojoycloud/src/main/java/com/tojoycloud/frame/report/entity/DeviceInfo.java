package com.tojoycloud.frame.report.entity;

public class DeviceInfo
{
	private String imei;

	private String imsi;

	private String language;

	private String model;

	private String is_emulator;

	private String serialno;

	private String manufacturer;

	private String sdk_level;

	private String screenHeight;

	private String screenWidth;

	private String device_name;

	public String getImei()
	{
		return imei;
	}

	public void setImei(String imei)
	{
		this.imei = imei;
	}

	public String getImsi()
	{
		return imsi;
	}

	public void setImsi(String imsi)
	{
		this.imsi = imsi;
	}

	public String getLanguage()
	{
		return language;
	}

	public void setLanguage(String language)
	{
		this.language = language;
	}

	public String getModel()
	{
		return model;
	}

	public void setModel(String model)
	{
		this.model = model;
	}

	public String getIs_emulator()
	{
		return is_emulator;
	}

	public void setIs_emulator(String is_emulator)
	{
		this.is_emulator = is_emulator;
	}

	public String getSerialno()
	{
		return serialno;
	}

	public void setSerialno(String serialno)
	{
		this.serialno = serialno;
	}

	public String getManufacturer()
	{
		return manufacturer;
	}

	public void setManufacturer(String manufacturer)
	{
		this.manufacturer = manufacturer;
	}

	public String getSdk_level()
	{
		return sdk_level;
	}

	public void setSdk_level(String sdk_level)
	{
		this.sdk_level = sdk_level;
	}

	public String getScreenHeight()
	{
		return screenHeight;
	}

	public void setScreenHeight(String screenHeight)
	{
		this.screenHeight = screenHeight;
	}

	public String getScreenWidth()
	{
		return screenWidth;
	}

	public void setScreenWidth(String screenWidth)
	{
		this.screenWidth = screenWidth;
	}

	public String getDevice_name()
	{
		return device_name;
	}

	public void setDevice_name(String device_name)
	{
		this.device_name = device_name;
	}

}
